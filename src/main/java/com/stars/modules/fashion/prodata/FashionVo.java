package com.stars.modules.fashion.prodata;

/**
 * 时装表Vo数据;
 * Created by gaopeidian on 2016/10/08.
 */
public class FashionVo {
    private int fashionId;
    private int type;
    private int job;
    private String icon;
    private int itemId;
    private int quality;
    private String name;
    private String resource;
    private String desc;
    private int showExpressId;

    public int getFashionId() {
        return fashionId;
    }

    public void setFashionId(int fashionId) {
        this.fashionId = fashionId;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getJob() {
        return job;
    }

    public void setJob(int job) {
        this.job = job;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public int getItemId() {
        return itemId;
    }

    public void setItemId(int itemId) {
        this.itemId = itemId;
    }

    public int getQuality() {
        return quality;
    }

    public void setQuality(int quality) {
        this.quality = quality;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getResource() {
        return resource;
    }

    public void setResource(String resource) {
        this.resource = resource;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    public int getShowExpressId() {
        return showExpressId;
    }

    public void setShowExpressId(int showExpressId) {
        this.showExpressId = showExpressId;
    }
}
