public class MarryRing {
    private int ringid;
    private String icon;
    private String name;
    private String desc;
    private byte quality;


    public int getRingid() {return this.ringid;}

    public void setRingid(int ringid) { this.ringid = ringid;}

    public String getIcon() {return this.icon;}

    public void setIcon(String icon) {this.icon = icon;}

    public String getName() {return this.name;}

    public void setName(String name) {this.name = name;}

    public String getDesc() {return this.desc;}

    public void setDesc(String desc) {this.desc = desc;}

    public byte getQuality() {return this.quality;}

    public void setQuality(byte quality) {this.quality = quality;}
}
