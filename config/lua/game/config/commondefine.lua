-- 根据key取配置
function CFG.commondefine:get(key)
	if CFG.isPrintNonExistLog and key ~= '0' and self.configs[key] == nil then
		TraceLog("配置commondefine[" .. key .. "]不存在！")
	end
	return self.configs[key]
end

-- 根据两对key来查找合并的结果
function CFG.commondefine:getByKeys(key1,val1,key2,val2)
	if key1 == nil and val1 == nil and key2 == nil and val2 == nil then
		return nil
	end
	local result = {}
	for k,v in pairs(CFG.commondefine.configs) do
		if v[key1] == val1 then
			if key2 == nil or v[key2] == val2 then
				table.insert(result,v)
			end
		end
	end
	return result;
end

-- 根据paramsTable中的参数来查找符合的配置如：{type = 1, sceneId = 2011}
function CFG.commondefine:getByParams(paramsTable)
	if paramsTable == nil then
		return CFG.commondefine.configs
	end
	local result = {}
	for k,v in pairs(CFG.commondefine.configs) do
		local hit = true
		for k1,v1 in pairs(paramsTable) do
			if v[k1] ~= v1 then
				hit = false
				break
			end
		end
		if hit then
			table.insert(result, v)
		end
	end
	return result;
end

-- 根据指定的条件函数来查找结果，条件函数返回true的将放入结果中
function CFG.commondefine:getByConditionFunc(func)
	local result = {}
	for k,v in pairs(CFG.commondefine.configs) do
		if func(v) == true then
			table.insert(result,v)
		end
	end
	return result;
end

-- 取全部配置
function CFG.commondefine:getAll()
	return self.configs
end

-- 运行时添加新的配置进来
function CFG.commondefine:addConfig(key, config)
	if key == nil or config == nil then return end
	if self.configs[key] ~= nil then return end
	self.configs[key] = config
end

